package site.nebulas.beans;

public class Dynamic {
	private Integer dynamicId;
	private String userAccount;
	private String dynamicLoginIp;
	private String dynamicContent;
	private String dynamicAddTime;
	private Integer dynamicTyle;
	public Integer getDynamicId() {
		return dynamicId;
	}
	public void setDynamicId(Integer dynamicId) {
		this.dynamicId = dynamicId;
	}
	public String getUserAccount() {
		return userAccount;
	}
	public void setUserAccount(String userAccount) {
		this.userAccount = userAccount;
	}
	public String getDynamicLoginIp() {
		return dynamicLoginIp;
	}
	public void setDynamicLoginIp(String dynamicLoginIp) {
		this.dynamicLoginIp = dynamicLoginIp;
	}
	public String getDynamicContent() {
		return dynamicContent;
	}
	public void setDynamicContent(String dynamicContent) {
		this.dynamicContent = dynamicContent;
	}
	public String getDynamicAddTime() {
		return dynamicAddTime;
	}
	public void setDynamicAddTime(String dynamicAddTime) {
		this.dynamicAddTime = dynamicAddTime;
	}
	public Integer getDynamicTyle() {
		return dynamicTyle;
	}
	public void setDynamicTyle(Integer dynamicTyle) {
		this.dynamicTyle = dynamicTyle;
	}
	
	
}
